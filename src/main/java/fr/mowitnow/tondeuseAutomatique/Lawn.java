package fr.mowitnow.tondeuseAutomatique;

/**
 * Réprésente la pelouse
 * @author Arthur
 *
 */
public class Lawn {
	
	private int length;
	private int width;
	
	public Lawn(int length, int width) {
		this.length = length;
		this.width = width;
	}

	public int getLength() {
		return length;
	}

	public int getWidth() {
		return width;
	}

}
